// Flat list of all categories for filtering
export const getAllCategories = (gender) => {
    const categories = gender === 'men' ? menCategories : womenCategories;
    const all = [];

    Object.values(categories).forEach(group => {
        group.items.forEach(item => {
            all.push({
                ...item,
                group: group.label
            });
        });
    });

    return all;
};

// Get category label by value
export const getCategoryLabel = (value, gender) => {
    const allCats = getAllCategories(gender);
    const cat = allCats.find(c => c.value === value);
    return cat ? cat.label : value;
};

export default { menCategories, womenCategories, getAllCategories, getCategoryLabel };
